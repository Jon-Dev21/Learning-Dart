/**
 * Learning about data type operators.
 */

void main() {
/**
 * Arithmetic Operators
 */

// + operator: adds 2 values
var sum = 10+10;
print("10 + 10 = $sum");

// - operator: subtracts 2 values
var sub = 10-10;
print("10 - 10 = $sub");

// * operator: multiplies 2 values
var mult = 10*10;
print("10 * 10 = $mult");

// - operator: divides 2 values
var div = 9/2;
print("9 / 2 = $div");

// ~/ operator: Integer division of 2 values(result is always an integer).
var intDiv = 9~/2;
print("9 ~/ 2 = $intDiv");

}
